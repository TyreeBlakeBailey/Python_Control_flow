#loops: while, for

# if statment
weather = "rain"

if weather == "sunny": #if the boolean value = true then run next line of code
    print("Enjoy the weather")
elif weather == "rain":
    print("Bring an umbrella, its raining")
elif weather == "cloudy":
    print("No sun, but lucky no rain yet")
else:
    print("Something went wrong")
